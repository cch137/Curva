<script setup>
import { marked } from 'marked'
import formatDate from '~/utils/formatDate'
import random from '~/utils/random'
import { DocumentCopy, ChatDotRound, User, Cpu, Search, Paperclip } from '@element-plus/icons-vue'
import '~/assets/css/vsc-dark-plus.css'

const { messages, openSidebar, sendMessage } = useChat()

marked.setOptions({ headerIds: false, mangle: false })

const more = reactive({
  start: 0,
  end: 3,
  step: 3,
  run (maxLength = 8) {
    if (more.end >= maxLength) {
      more.reset()
    } else {
      more.start += more.step
      more.end += more.step
    }
  },
  reset () {
    more.start = 0
    more.end = more.step
  }
})

watch(messages, () => more.reset())

const loadingDots = ref('.')

setInterval(() => {
  if (loadingDots.value.length < 4) {
    loadingDots.value += '.'
  } else {
    loadingDots.value = '.'
  }
}, 500)
</script>

